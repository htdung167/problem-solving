# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution(object):
    def removeNthFromEnd(self, head, n):
        """
        :type head: ListNode
        :type n: int
        :rtype: ListNode
        """
        node = head
        count = 0
        while node != None:
            node = node.next
            count += 1
        if n == count:
            return head.next
        node = head
        i = count - n - 1
        while i > 0:
            i -= 1
            node = node.next
        node.next = node.next.next
        return head
        
